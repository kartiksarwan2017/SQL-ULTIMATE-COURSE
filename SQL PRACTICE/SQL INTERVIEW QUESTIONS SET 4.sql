-- CORRECT WAY
/*
Your query

GROUP BY
product

So you're getting
one row per product

instead of
one row per category.

*/
SELECT
    p.category,
    COUNT(DISTINCT p.id) AS Products,
    AVG(p.list_price) AS AveragePrice,
    SUM(od.unit_price * od.quantity * (1-od.discount)) AS Revenue
