/*
Q10
Display the total revenue generated by each category.
Output
CategoryName
Revenue
*/
SELECT category AS CategoryName,
       SUM(list_price) - SUM(standard_cost) AS Revenue
FROM products
GROUP BY category;


/*
This one is the biggest mistake.
You calculated
SUM(list_price)-SUM(standard_cost)
That is potential profit, not revenue.
Revenue is
Quantity × Unit Price
In Northwind it comes from
order_details

*/

-- CORRECT QUERY
SELECT
    p.category,
    SUM(od.quantity * od.unit_price) AS Revenue
FROM order_details od
JOIN products p
ON od.product_id=p.id
GROUP BY p.category;


