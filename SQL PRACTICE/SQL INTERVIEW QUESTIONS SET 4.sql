/*
Q1
Display all customers who have placed at least one order.
Output:
CustomerID
CompanyName
OrderID
OrderDate
*/
SELECT c.id AS CustomerID,
       c.company AS CompanyName,
       o.id AS OrderID, 
       o.order_date AS OrderDate
FROM customers c
INNER JOIN orders o
ON c.id = o.customer_id;



/*
Q2
Display all employees who handled orders shipped to Germany.
Output:
EmployeeName
OrderID
ShipCountry
*/
SELECT CONCAT(e.first_name, ' ', e.last_name) AS  EmployeeName,
	   o.id AS OrderID,
       o.ship_country_region AS ShipCountry
FROM employees e 
INNER JOIN orders o 
ON e.id = o.employee_id
WHERE o.ship_country_region = 'Germany';


